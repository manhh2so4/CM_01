	public static string GetNameStat(StatType statType , int value) {
		int size = 40;
		switch (statType) {
			case StatType.damage:
				return $"Sát thương : <size={size}>+{value}";
			case StatType.damagePercent:
				return $"Sát thương : <size={size}>+{value}%";
			case StatType.critChance:
				return $"Tỉ lệ chí mạng : <size={size}>+{value}%";
			case StatType.critDamage:
				return $"Sát thương chí mạng : <size={size}>+{value}%";
			case StatType.cooldown:
				return $"Hồi chiêu : <size={size}>{value}s";
//----------------------
			case StatType.fireDame:
				return $"Hỏa công : <size={size}>+{value}";
			case StatType.iceDame:
				return $"Băng công : <size={size}>+{value}";
			case StatType.electricDame:
				return $"Phong lôi : <size={size}>+{value}";
//----------------------
			case StatType.health:
				return $"Máu tối đa : <size={size}>+{value}";
			case StatType.healthPercent:
				return $"Phần trăm Máu: <size={size}>+{value}%";
			case StatType.mana:
				return $"Mana tối đa : <size={size}>+{value}";
			case StatType.manaPercent:
				return $"Phần trăm Mana: <size={size}>+{value}%";
			case StatType.armor:
				return $"Giảm sát thương : <size={size}>{value}";
//----------------------
			case StatType.ExpPercent:
				return $"Thêm kinh nghiệm : <size={size}>+{value}%";
			case StatType.SpeedPercent:
				return $"Tốc độ di chuyển: <size={size}>+{value}%";
//----------------------
			case StatType.BurnTime:
				return $"Thời gian cháy : <size={size}>+{value}s";
			case StatType.FreezeTime:
				return $"Thời gian đóng băng : <size={size}>+{value}s";
			case StatType.ShockTime:
				return $"Thời gian điện giật : <size={size}>+{value}s";
		}
		return statType.ToString();
	}
	public static Color GetColorStat(StatType statType) {
		switch (statType) {
			case StatType.cooldown:
				return Gray;
			case StatType.damage:
				return White;
			case StatType.damagePercent:
				return BlueWhite;
			case StatType.critChance:
				return Blue;
			case StatType.critDamage:
				return BlueWhite;
//----------------------
			case StatType.fireDame:
				return Red;
			case StatType.iceDame:
				return Cyan;
			case StatType.electricDame:
				return Yellow;
//----------------------
			case StatType.health:
				return GreenDrak;
			case StatType.healthPercent:
				return Green;
			case StatType.mana:
				return GreenDrak;
			case StatType.manaPercent:
				return Green;
			case StatType.armor:
				return Blue;
//----------------------
			case StatType.ExpPercent:
				return Cyan;
			case StatType.SpeedPercent:
				return Cyan;
//----------------------
			case StatType.BurnTime:
				return Red;
			case StatType.FreezeTime:
				return Cyan;
			case StatType.ShockTime:
				return Yellow;
		}
		return Color.white;

	}
